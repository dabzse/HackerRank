def organizingContainers(container):
    # Write your code here
    sum_row = [sum(row) for row in container]
    sum_col = [sum(col) for col in zip(*container)]
    sum_row.sort()
    sum_col.sort()

    if sum_row == sum_col:
        return "Possible"
    else:
        return "Impossible"

