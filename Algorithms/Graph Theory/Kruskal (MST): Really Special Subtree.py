class UnionFind:
    def __init__(self, n):
        self.parent = list(range(n))
        self.rank = [0] * n

    def find(self, u):
        if self.parent[u] != u:
            self.parent[u] = self.find(self.parent[u])
        return self.parent[u]

    def union(self, u, v):
        root_u = self.find(u)
        root_v = self.find(v)
        if root_u != root_v:
            if self.rank[root_u] > self.rank[root_v]:
                self.parent[root_v] = root_u
            elif self.rank[root_u] < self.rank[root_v]:
                self.parent[root_u] = root_v
            else:
                self.parent[root_v] = root_u
                self.rank[root_u] += 1

def kruskals(g_nodes, g_from, g_to, g_weight):
    # Write your code here
    edges = []

    for i, (u, v, weight) in enumerate(zip(g_from, g_to, g_weight)):
        edges.append((weight, u - 1, v - 1))

    edges.sort()

    uf = UnionFind(g_nodes)
    mst_weight = 0
    mst_edges = 0

    for weight, u, v in edges:
        if uf.find(u) != uf.find(v):
            uf.union(u, v)
            mst_weight += weight
            mst_edges += 1
            if mst_edges == g_nodes - 1:
                break

    return mst_weight


